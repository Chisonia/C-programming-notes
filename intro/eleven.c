#include <stdio.h>
//operators
void main(){
    int a = 10;
    int b = 5;
    int c = 2;

    printf("a+b: %d\n", a+b);
    printf("a-b: %d\n", a-b);
    printf("a/b: %d\n", a/b);
    printf("a*b: %d\n", a*b);
    printf("b/c: %d\n", b/c);
    printf("b/c: %f\n", (float)b/c);
    printf("a/b remainder: %d\n", a%b);
    printf("b/c remainder: %d\n", b%c);
}